package clasess;

public class Lugar {
    private int id;
    private String lugar;

   

    public void setId(int id) {
        this.id = id;
    }

    public void setDato(String lugar) {
        this.lugar = lugar;
    }

    
    public String getDato() {
        return this.lugar;
    }

    public int getId() {
        return this.id;
    }
    
}
