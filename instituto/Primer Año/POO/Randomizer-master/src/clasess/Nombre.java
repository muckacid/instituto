package clasess;

public class Nombre {
    private int id;
    private String nombre;
    


    public void setId(int id) {
        this.id = id;
    }

    public void setDato(String nombre) {
        this.nombre = nombre;
    }
    

    public int getId() {
        return this.id;
    }

    public String getDato() {
        return this.nombre;
    }
    
    
}
